                    <h2>Europa</h2>
                    <p>Rakiety trafiły do Europy wraz z Mongołami. Pierwszymi Europejczykami, którzy mieli do czynienia z rakietami byli Rosjanie, Austria oraz kraje Europy Wschodniej, które zostały podbite przez Czyngis-chana. Warto wspomnieć, że rakiet używało również Imperium osmańskie podczas upadku Konstantynopola. Z rakietami eksperymentował polski inżynier wojskowy i artylerzysta Kazimierz Siemienowicz (ok. 1600-1651). W 1650 roku opublikował w Amsterdamie fundamentalne dzieło "Artis Magnae Artilleriae pars prima" ("Wielkiej sztuki artylerii część pierwsza"), która przez prawie 200 lat stanowiła podstawowy podręcznik artylerii w Europie.W roku 1903 Konstanty Ciołkowski, rosyjski uczony polskiego pochodzenia w artykule wyłożył teorię lotu rakiety z uwzględnieniem zmiany masy (pierwsza poważna praca z dziedziny astronautyki).</p>
