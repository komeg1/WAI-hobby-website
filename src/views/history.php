                    <h2>Historia teraźniejsza</h2>
                    <p>W roku 1917 Robert Goddard z Smithsonian Institution w USA opatentował wynalazek poprawiający w znaczny sposób wydajność rakiety. Dodał on specjalne dysze do komory spalania silnika rakietowego powodujące wypływ gazów z prędkością naddźwiękową (dysza de Laval).Od 1937 roku w Peenemünde na wyspie Uznam w Niemczech istniał ośrodek badawczo-rozwojowy. Od roku 1943 Niemcy prowadziły seryjną produkcję pierwszego rakietowego pocisku balistycznego V-2. Po wojnie ZSRR, Anglia i USA prześcigały się w pozyskaniu technologii niemieckiej. Po kapitulacji Niemiec, von Braun wraz ze współpracownikami poddał się wojskom amerykańskim, licząc na możliwość kontynuowania prac nad rakietami do celów cywilnych. Amerykańskie władze, w obliczu rysującej się konfrontacji z państwami komunistycznymi, chętnie przyjęły von Brauna, jak również część jego zespołu i ocalałe części rakiet V-2. Związek Radziecki dla przechwyconych przez siebie niemieckich specjalistów programu V-2 utworzył pierwotnie ośrodek naukowo-badawczy Instytut Rabe w Nordhausen, gdzie mieli kontynuować swoją pracę, jednakże 22 października 1946 roku NKWD aresztowało ich wraz z rodzinami oraz specjalistami innych dziedzin techniki wojskowej, i tę grupę około pięciu tysięcy osób wywieziono w głąb ZSRR, gdzie mieli kontynuować swoje prace pod ścisłym nadzorem. W ten sposób zapoczątkowany został zimnowojenny wyścig rakietowy, którego ukoronowaniem była misja Apollo na Księżyc.</p>
